using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace BlExponencial
{
    public class ClsExponencial
    {
        /// <summary>
        /// Metodo Exponencial 
        /// </summary>
        /// <param name="Num1"></param>
        /// <param name="Num2"></param>
        /// <returns></returns>
        public static long Exponencial(long Num1, long Num2)
        {
            return (Num1 ^ Num2);
        }
    }
}
