using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace BlHipotenusa
{
    public class ClsHipotenusa
    {
        
        public static Double Hipotenusa (Double Numero1, Double Numero2)
        {

            return Math.Sqrt((Numero1 * Numero1) + (Numero2 * Numero2));
        }


    }
}
